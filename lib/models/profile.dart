class Profile {
  int id; // ID unik untuk setiap profil
  String name;
  String nim;
  String kelas;
  String sd;
  String smp;
  String smk;
  String kampus;
  String image;
  String background;

  Profile({
    required this.id,
    required this.name,
    required this.nim,
    required this.kelas,
    required this.sd,
    required this.smp,
    required this.smk,
    required this.kampus,
    required this.image,
    required this.background,
  });
}
